	<h1>Introduction</h1>
	<p>The <i>Core Factors Career Path</i> assessment aims to provide structure to your attitudes
	  about work, what you prefer and what you would like to avoid, and the tasks, activities,
	  and environments in which the work is done.</p>
	<p> Whether you are starting out or changing careers, making the right career choice is
	  one of the most important decisions that we all make. We rely upon our careers to
	  meet many of our personal needs, including financial security, identity, and
	  satisfaction. Unfortunately, the process necessary to choose the “right” career or
	  occupation is unclear.</p>
	<p> This report will help you put structure into your decision-making process. You will
	  learn how to identify the possible occupations that can lead to a satisfying career
	  experience and reduce the chance that you will be surprised later by unexpected
	  disappointments. This report will not tell you “who you are” or what occupation or
	  career you should seek; it provides relevant information to help you make a better
	  decision regarding a career path or change.</p>
