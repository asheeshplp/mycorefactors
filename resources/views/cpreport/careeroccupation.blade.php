	<h1>Occupational Activity Groupings (OAG)</h1>
	<p>The following are general descriptions of the eleven Occupational Activity Groupings (OAG). While the number of new tasks and
	  activities is continuously increasing, these 11 areas cover much of what is commonly found in work and leisure today. Read the
	  descriptions carefully before going on to your individual OAG profile. You will also want to refer back to them often</p>
	
	<div class="info-career-activity pt-3" id="business-management">
		<h2 class="heading-inner-comman">Business/Management</h2>
		<div class="d-flex justify-content-between gap-5 align-items-center">
		  <p>Business/Management tasks and activities involve leading and directing organizations and individuals in the
			day-to-day and/or long-range processes of operating an organization or unit (division or section of an
			organization). They may include but are not limited to managing a private business, corporation executives,
			and leadership roles in civic organizations and projects. Common tasks and activities include oversight of
			procedures and activities necessary to carry out the mission and vision of the organization. There tends to be
			frequent if not continuous, direct involvement with individuals as well as a high level of responsibility for
			success. The environment in which these tasks are performed is typically an inside office facility, yet may
			involve frequent travel and meetings in other environments. Individuals preferring this area are typically
			comfortable making decisions that affect the lives and future of others.</p>
			<img src="{{ asset('img/Management.png') }}" width="120" height="120" alt="business-management" />
		</div>
	</div>
	<div class="info-career-activity pt-3" id="business-financial">
	  <h3 class="heading-inner-comman">Business/Financial</h3>
	  <div class="d-flex justify-content-between gap-5 align-items-center">
		<p>Business/Financial Tasks and Activities involve the interaction with and responsibility for budgetary and
		  financial resource development and utilization. These thought-based activities involving the use of
		  mathematics and statistics serve to inform individuals and organizations of their financial status and financial
		  resource management. Involvement with computers and software is common throughout the range of
		  occupations in this area. The work environment in which these tasks and activities are performed is nearly
		  always indoor office space with limited interruption and interaction with others.</p>
		  <img src="{{ asset('img/Financial.png') }}" width="120" height="120" alt="business-management" />
	  </div>
  </div>
  <div class="info-career-activity pt-3" id="digital-data">
	<h4 class="heading-inner-comman">Digital Data</h4>
	<div class="d-flex justify-content-between gap-5 align-items-center">
	  <p>Digital Data Tasks and Activities include the development, maintenance, and utilization of information on
		computer platforms. This includes using computers to develop methods of meeting the needs within
		businesses and organizations for data and resource management as well as individual communication and
		dissemination of information. The environment within which these tasks are performed involves a desk and a
		computer. There may be limited meetings with other individuals as projects are developed and limited
		environmental interruptions, which facilitates the thinking processes necessary for this work. People who
		prefer these activities and tasks will enjoy hours of concentrating on problem-solving and creating ways of
		using and managing information.</p>
		<img src="{{ asset('img/digital-data.png') }}" width="120" height="120" alt="business-management" />
	</div>
</div>
<div class="info-career-activity pt-3" id="mechanical">
  <h5 class="heading-inner-comman">Mechanical</h5>
  <div class="d-flex justify-content-between gap-5 align-items-center">
	<p>Mechanical Tasks and Activities tend to involve thought and action used to move, manipulate, and construct in
	  the physical environment. This area will frequently involve a lot of physical motion and activity during
	  interaction with the physical world. Frequently, knowledge of machinery and tools is necessary to perform the
	  tasks and activities associated with jobs in this area. The environment in which these tasks and activities are
	  frequently performed is physically large. It may be outdoors, industrial, or factory-like settings. People
	  preferring this area of tasks and activities will typically be successful at designing and producing tangible
	  objects and results.</p>
	  <img src="{{ asset('img/mechanical.png') }}" width="120" height="120" alt="business-management" />
  </div>
</div>

<div class="info-career-activity pt-3" id="scientific">
<h3 class="heading-inner-comman">Scientific</h3>
<div class="d-flex justify-content-between gap-5 align-items-center">
  <p>Scientific Tasks and Activities focus on exploratory processes where mental activities are typically used to
	discover or describe unknown information. These tasks and activities may be performed in indoor or outdoor
	environments (laboratories). Due to the intense mental activities associated with this area, the setting is
	frequently quiet, with minimal intrusion and interaction with others. Occupations involving this area
	frequently require advanced education or training.</p>
	<img src="{{ asset('img/scientific.png') }}" width="120" height="120" alt="business-management" />
</div>
</div>

<div class="info-career-activity pt-3" id="artistic">
<h4 class="heading-inner-comman">Artistic</h4>
<div class="d-flex justify-content-between gap-5 align-items-center">
  <p>Artistic Tasks and Activities are associated with creativity of all forms. The tasks and activities in this area range
	from quiet, isolated production of fine artistic (painting, sculpting, writing) works to group performance in
	front of large audiences or a deep appreciation of any artistic pursuit. Central to all tasks and activities in this
	area is the desire to produce and create something new and different that is typically shared with others.
	There is a wide range of artistic medium production, from purely mental, as in writing, to physical, as in
	performing arts (ballet). There is an equally wide range of environments, from quiet and serene to crowded
	and chaotic.</p>
	<img src="{{ asset('img/artistic.png') }}" width="120" height="120" alt="business-management" />
</div>
</div>

<div class="info-career-activity pt-3" id="social-group">
<h5 class="heading-inner-comman">Social/Group Involvement</h5>
<div class="d-flex justify-content-between gap-5 align-items-center">
  <p>This area of task and activity is involved with serving the needs and desires of others in groups or teams. A
	common theme in this area is the experience of being part of a collection of individuals with a common
	purpose or goal. It may involve one individual serving a group or a group serving an individual. In either case,
	it is not typically a singular or one-on-one activity. Because of the consistent group activity, most occupations
	associated with this area involve physical activity and verbal interaction. The environments in which these
	activities occur may range from an office-type space to an out of doors athletic field.</p>
	<img src="{{ asset('img/social-group-involvement.png') }}" width="120" height="120" alt="business-management" />
</div>
</div>

<div class="info-career-activity pt-3" id="home-nature">
<h4 class="heading-inner-comman">Home and Nature</h4>
<div class="d-flex justify-content-between gap-5 align-items-center">
  <p>Home and Nature Tasks and Activities are focused on exploring and impacting the natural world, including our
	home environments. They may range from aesthetic acts of home remodeling/flower gardening and
	landscaping to exploring the geology of rock formations or forestry management. The activities typically
	involve interactions with smaller numbers of people or family units and require reflective and creative
	thought. Further, there is frequent involvement with building or making something in a natural setting.
	Central to this area is a desire to be involved in a more native or natural environment, including oneâ€™s home.</p>
