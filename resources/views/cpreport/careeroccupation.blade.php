<div class="d-flex justify-content-between gap-5 align-items-center">
  <p>Government Service Tasks and Activities represent a variety of occupations within the structure and
	organization of a governmental organization or body. The most common element of this area is the existence
	of a highly structured organization unit where clear guidelines describe all of the tasks and activities, and the
	chain of authority is articulated and promoted as part of the structure itself. There is very little ambiguity or
	flexibility within this areaâ€™s occupations regarding what tasks and activities an individual will perform. People
	who prefer governmental service tasks and activities will find themselves in environments that range from
	office buildings to police cars. </p>
