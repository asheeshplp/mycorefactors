<h1>Career Basics</h1>
<p>Your career choices will become an essential part of who you are. We all have experiences that we like and are interested in or
  dislike and/or find uncomfortable, but to connect those experiences in a structured or systematic framework is not something
  we naturally do. The process presented in this report will not tell you what you will do with your life, but it will provide you with a
  better understanding of yourself in connection with the world of work. This is a unique process that helps you know where you
  might be headed and what you need to avoid.</p>
<p>The <i>Core Factors Career Path</i> report reveals your preference and avoidance pattern for the Occupational Activity Groupings,
  which consist of groups of tasks, activities, and environments in which they are performed, and your Global Interest Area Code,
  which are results that relate your work personality to different working environments.</p>
<div class="text-center py-5"><img class="w-80" src="{{ asset('img/career-basics-img.png') }}" alt=""></div>
<p class="mb-4">This report will allow you to closely examine and match them to general characteristics of everyday occupational tasks, activities,
  and career environments. Remember that the structured information describes parts of your overall pattern and is not part of
  any complex theory. It will not tell you what to do or force you to make a career decision. It is based on your responses across
  eleven Occupational Activity Groupings (OAG) and six Global Interest Areas (GIA). You will see how your individual Prefers and
  Avoids patterns across the OAGs match different occupational tasks, activities, and environments in which they occur. Your work
  personality profile on the GIAs will allow you to compare yourself with similar work environments. Throughout this report, you
  will be given information to help you make a career decision that is satisfying and a good fit for your unique self</p>
