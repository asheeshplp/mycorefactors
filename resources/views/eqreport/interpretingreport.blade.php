<div id="effectiveness-scores">
<h3>Effectiveness Scores</h3>
<p>Look at each of your EQ Skill Effectiveness Scores in each Domain. Are there particular patterns that emerge in each Domain and across Domains? For example, are the self-ratings generally the same in each Domain or are there variations where you scored a 4 and others a 7?</p>
<p>In cases where you gave essentially the same rating for each skill, consider how to verify how reflective this may be of experience or things learned over time in this Domain. And depending on how Important a particular skill may be, you may want to consider ways to enrich or deepen that skill. In cases with score variations such as a 4 and 7 in a domain, reflect on how experience and feedback (or lack thereof) have played into how these skills are demonstrated.</p>
<p>Moderately or lower-rated EQ Skills may need to be further developed, depending on relative importance at this time.</p>
</div>
<div id ="importance-scores">
<h4>Importance Scores</h4>
<p>Look at your EQ Skill Importance Scores in each Domain. Are there particular patterns that emerge in each Domain and across Domains? While the guidelines for interpreting the Effectiveness scores are similar, the difference is that you are indicating how Important a skill may be, and as such need to consider why a particular skill is Important now and may be in the future.</p>
<p>Moderately or lower-rated Importance with EQ Skills may need to be explored and others’ feedback examined to determine the relative value of this skill for you now.</p>
</div>
<div id="gap-scores">
<h5>Gap Scores</h5>
<p>Absolute Gap Scores are reported. It does not matter if the Effectiveness Score is higher or the Importance Score is higher, a “Gap” is the difference between the two self-ratings. If the Importance score is higher, it becomes more valuable to explore why this may be true. Only those Gap Scores that are high and the Importance rating is higher than the Effectiveness Score, will the system highlight the skill. The action tips provided at the end of the report are ONLY for those highlighted skills.</p>
<p>If you feel you are very Effective at a skill that may not be Important right now, consider that “money in the bank.” When you need that skill in the future, you are relatively ready to demonstrate it.</p>
