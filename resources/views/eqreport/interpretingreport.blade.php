<p>A star (*) denotes that Effectiveness was rated higher than Importance, suggesting this particular behavior may be adequate for the current situation. It is a good idea to get additional feedback about your behavior to confirm how effective you are at demonstrating a particular skill. You can ask someone who knows you well a simple question like, “Tell me about a time when you saw me do…" These EQ skills are like having money in a savings account.</p>
