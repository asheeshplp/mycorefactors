<h3>Interpreting Your Report</h3>
<p>The goal of any interpretation is to provide clarity with the data and focus on those development areas that will benefit you the most. You have four kinds of information to consider when interpreting your report: Dimension Scores, Effectiveness and Importance Scores, and Gap Scores. It is essential that you look at themes and patterns in your scores, identifying those areas that appear to be strengths and areas needing attention.</p>
<p>Keep in mind that all of your behaviors are influenced by your family of origin, culture, situational pressures, previous lessons learned, and dispositions---what you came into the world with such as tendencies and traits. When you are considering the nature and aspects of your behavior, keep in mind that each is complex and malleable—you can adjust.</p>

