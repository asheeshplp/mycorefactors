<h2>Domain Scores</h2>
<p>Your Domain score is the addition of each of the ten EQ skill scores you provided. The ten EQ skills are summed for both Effectiveness and Importance such that the lowest score is 10 and the highest score is 70. The averages provided by the norm group allow for an immediate comparison of how similar or dissimilar your reported scores are to a similar adult group. The Interpretative note is based on your scores falling within certain ranges as allowed by the standard deviations in the norm group—or average differences. So, if your score is within the typical range of most adults, your interpretative comment will provide one kind of guidance, and if higher or lower by a standard deviation from the typical adult group, you receive another interpretative comment</p>
<p>While it is likely that those Domains with the highest or lowest scores will reflect the higher or lower specific EQ skills, that may not always be true. For example, if your highest Domain is Self-Awareness there may be several EQ skills in that domain with lower Effectiveness or Importance scores. Overall scores that the Domains provide tell us “the way the wind is blowing.” The specific EQ skills scores give clues as to “how strong the winds are.”</p>
