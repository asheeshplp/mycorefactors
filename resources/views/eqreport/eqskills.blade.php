                                <h4>An Overall Picture</h4>
                              <p>
                                These four dimensions are considered foundational to Emotional Intelligence (EQ):
                              </p>

                              <div class="row mb-4">
                                <div class="col-md-6 pe-0">
                                  <div class="tab-box h-100 rounded-0 ">
                                    <div class="box-title-comman box-title-bg-1 rounded-0">Self-Awareness Perceptions</div>
                                    <p class="p-3">Self-Awareness means you accurately perceive and understand your own thoughts, emotions, and behaviors. Self-aware individuals are conscious of their emotions and reactions Being self-aware means having an accurate self-assessment of confidence, authenticity, integrity, independence, resilience, stamina, self-actualization, and mindfulness. SelfAwareness is a cornerstone to development and effectiveness.</p>
                                  </div>
                                </div>
                                <div class="col-md-6 ps-0">
                                  <div class="tab-box h-100 rounded-0">
                                    <div class="box-title-comman box-title-bg-2 rounded-0">Other Awareness Perceptions</div>
                                    <p class="p-3">Other-Awareness means being socially aware of others’ feelings, perspectives, and points of view. This awareness leads to demonstrating compassion, reading non-verbal behavior, being flexible to others and being insightful about their needs in each situation Understanding others through re-framing and being socially intelligent promotes insight into others behavior.</p>

                                  </div>
                                </div>
                                <div class="col-md-6 pe-0">
                                  <div class="tab-box h-100 rounded-0">
                                    <div class="box-title-comman box-title-bg-3 rounded-0">Self-Regulation</div>
                                    <p class="p-3">Self-Regulation is the ability to manage and control one’s thoughts, emotions, and behaviors in order to adapt to different situations and interactions. Self-regulation is demonstrated by emotional self-control, impulse control, adaptability, being tolerant and optimistic about dealing with others with intention and openness.</p>

                                  </div>
