		<h1>Understanding Your Three "Selves"</h1>
		<p>As you consider the content of this report it can be useful to keep in mind the dynamics at play with who you are in your
		  current reality. You are a dynamic, creative, resourceful person and your life has presented you with experiences that you bring
		  to every situation and interaction. It is important to pay attention to these different aspects of your "self" throughout the
		  process of clarifying your Social Dynamics style.</p>

		  <div id="natural-self" class="mb-3 mt-4">
			<h2 class="heading-inner-comman">Your Natural Self</h2>
			<p>The aspects of your "natural self" are instinctive, reflexive and innate. When you operate out of your natural self, there tends to
			  be a sense of ease, enjoyment and natural motivation.</p>
		</div>
		<div class="d-flex justify-content-start">
		  <div class="left-content w-75">
			<div id="developed-self" class="mb-3 mt-1">
			  <h2 class="heading-inner-comman">Your Developed Self</h2>
			  <p>The aspects of your "developed self" are those that you
				have learned and practiced to the point that they are
				second nature. When you are drawing on your
				developed self, the behavior you are displaying may be
				easy. However, there is no great enjoyment or
				satisfaction associated with it.</p>
			</div>
			<div id="situational-self" class="mb-3 mt-4">
			  <h2 class="heading-inner-comman">Your Situational Self</h2>
			  <p>The aspects of your "situational self" are required by
				specific situations or circumstances, such as your job,
				your role on a team or your responsibilities on a
				project. When you operate out of your situational self,
				you may have a degree of skill. However, having to
				continue the behavior very long is either draining or
				frustrating.
				</p>
		  </div>                          
	   
		  <p>Distinguishing between these can help you better
			identify your natural Social Dynamics style.
			</p>
	  
