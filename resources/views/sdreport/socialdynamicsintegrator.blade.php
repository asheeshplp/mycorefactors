	<h1>The Integrator Style</h1>
	  
	<div class="row mt-2 mb-3">
	   <div class="col-12 mt-3">
		  <div class="d-flex align-items-start gap-3">
		  <div class="rigt-para"><p>Integrators are motivated by the drive―the insistent need―to integrate multiple sources of
			information, in pursuit of the highest possible degree of quality. As a result, they experience great
			satisfaction when they can reconcile many inputs and arrive at the insight, solution, or decision that
			leads to an exceptional result. </p> <p>This does not mean that Integrators are always unwilling to accept less than perfection. They
			  know that in a world of deadlines and short windows of opportunity, sometimes a "good enough,"
			  quick response is what’s needed. However, they will be seeking to make sure that the result is still
			  the best possible, given the constraints of the circumstances.
		  </p>
		 </div>
		 <img src="{{ asset('img/Integrator-img.png') }}" class="auto" height="auto" alt="mapper" style="width: 130px;">
		</div>
		  
		  <p>Integrators are thus drawn to environments and projects which allow them the time to access numerous sources of
			information, and to reconcile and synthesize the input they have gathered. The speed at which Integrators process and
			integrate information varies. However, they all share the need to seek the best outcome possible, based on multiple sources of
			input.
			</p>

		  <p>The integration process is internal, one which requires that Integrators have the opportunity to disconnect from the external
			environment. They may do this by physically going away to think and reflect. They may also stay physically present, but
			disengage psychologically from what’s going on around them to find space to reflect. This can frequently make them seem
			preoccupied or disengaged.</p>

		  <p>It is not uncommon that, following a meeting or a conversation, Integrators will carry on synthesizing what was discussed
			and end up refining and improving the decision or conclusion that had been reached. They may sometimes discover an
			altogether new and superior way of doing something. It would be wise, for those who may be frustrated with revisiting
			decisions, to consider what the Integrator is offering. Otherwise, the final output will likely be of lower quality than it could have
			been.
			</p>

		  <p>Integrators typically possess vast amounts of information. They love absorbing information and are constantly alert to new
			sources. These sources could be colleagues, books, online resources, or their personal network. Integrators are frequently
			connected to others outside of their organization and even their industry. Through personal connections, online social
			networks, or professional associations, they typically maintain a broad range of contacts on whom they can call. Their typical
			demeanor – congenial, approachable, patient, and curious – aids them in creating and maintaining these connections.
			</p>

		  <p>Integrators are biased towards making consultative decisions. They are patient in accumulating, discussing and synthesizing
			the information. Integrators prefer to be the ones making the decision. However, they want to base it on input from all relevant
			sources. Their energy has been so focused on their internal process of integration, that they sometimes forget to let people
			know what they have concluded or discovered after having consulted them.
			</p>

		  <p>The Integrator’s use of consultative decision-making is rooted in the assumption that if they have numerous and diverse
			sources of information and a sufficient amount of time, they can always determine what the best result would be in a given
			situation.
		  </p>

		  <p>Integrators possess natural talents that support their drive to reconcile and synthesize multiple sources of information. They
			are effective listeners, who absorb information naturally. Integrators have tremendous patience for the group process. They
			naturally notice who has not spoken in a group and know how to draw out quieter members. They have a keen ability to define
			terms and concepts clearly. Integrators also possess the capacity to reconcile diverse viewpoints and seemingly contradictory
			information.</p>    

		  <p class="mb-4">A common misperception of Integrators is that they are perfectionists who ignore deadlines and take forever to get things
			done. This may be true of immature or highly stressed Integrators. However, it is not true of most. In fact, the great majority of
			Integrators are very capable of working within deadlines and are willing to settle for the best possible, given the situation. It is
			true that they will likely be left with a feeling of, "If only we had had more time, it could have been better." What Integrators will
			do is push their teams to deliver the very best possible results within the limitations under which they are working.
			</p>                              
		
