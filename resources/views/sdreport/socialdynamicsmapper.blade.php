	<h1>The Mapper Style</h1>
	  
	<div class="row mt-2 mb-3">
	   <div class="col-12 mt-3">
		  <div class="d-flex align-items-start gap-3">
		  <div class="rigt-para"><p>Mappers are motivated by the drive–the earnest need–to determine the desired goal or outcome,
			and then map out a clear course of action to accomplish it. As a result, they experience deep
			satisfaction when they are able to see that things are progressing along the chosen route towards
			the goal. </p> <p>Mappers experience an inner tension, until the goal and the optimal route to achieving it have
			been decided. Once these are in place, that tension is transformed into a continual push to see progress and movement towards the desired goal.
		  </p>
		 </div>
		 <img src="{{ asset('img/mapper.png') }}" class="auto" height="auto" alt="mapper" style="width: 130px;">
		</div>
		  
		  <p>Mappers are biased towards consciously considered decisions. Their decision-making process
			needs to be based on solid information. Sufficient time must be allotted for its due consideration.
			Mappers are seeking to determine what goal or outcome is most needed or beneficial for
			themselves or their group. They place a high priority on the process of analysis and reflection. Once
			the goal is determined, they will apply the same consideration in developing a clear and highly
			efficient course of action to achieve the goal. Mappers need time to disengage from their external
			environment to reflect, in order to make considered decisions. They resist being hurried or
			pressured in their decision-making process.</p>

		  <p>The degree of detail they envision in their chosen course of action varies between Mappers. At one end of the spectrum are
			those who want a highly detailed plan that lays out a thorough step-by-step process. At the other end are those who simply
			want to know the general plan and the key milestones along the way that will let them know they are on track. What all
			Mappers share is the need for a clear course to be mapped in advance of taking action.</p>

		  <p>Mappers are generally happy to allow another individual to determine the desired goal or outcome, when that is
			appropriate. They will, however, expect that individual or group to have determined the goal in advance and to come prepared
			with a clear and considered course of action.</p>

		  <p>This does not mean that Mappers are unwilling to be part of a group process for determining the goal and plan. They will
			  gladly do so. However, they will want this purpose to be explicit before the group meets. They will also want others to come
			  prepared with the relevant data and information that will allow the group to consider a variety of factors in selecting the optimal
			  goal and plan.</p>

		  <p>In such planning sessions, when Mappers will verbalize their insights into potential difficulties in reaching a particular goal, or
			their concerns about a proposed course of action, they are often viewed as being negative or pessimistic. In reality, Mappers are
			demonstrating that they are on board. They are offering the group valuable information– which the group ignores to its own
			detriment.</p>

		  <p>Once the course of action to achieve the goal is clear, Mappers need to see movement and progress towards the goal. They
			work to keep the group on track, often refocusing others when they seem to be straying too far off course. They are frequently
			the individuals in meetings who remind the group that they have strayed from the agenda or suggest that it is time to move on
			to the next item.
		  </p>

		  <p>Mappers naturally possess the talents to support their drive to determine the most advantageous goal and the process to
			accomplish it. They possess a keen ability to clearly envision the goal that they desire to achieve. This is paired with a natural gift
			for analyzing multiple factors and considerations in mapping out the most effective means of achieving that goal. As implied
			above, Mappers naturally monitor progress towards the goal, adjusting as necessary to stay on track. Mappers also possess the
			ability to foresee potential problems or obstacles–including how people will respond and react–and plan accordingly.</p>
		  
		  <p class="mb-4">A common misperception of Mappers is that they are inflexible and unwilling to alter the plan once it’s set. That may be true
			of immature or highly stressed Mappers, but it is not true of the vast majority. While Mappers are oriented towards staying with
			the program that has been decided, they are willing to readjust and even abandon a course of action if new information
			becomes available, or circumstances change and alter its relevance.</p>
