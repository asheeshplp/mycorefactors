	<h1>The Mover Style</h1>
	  
	<div class="row mt-2 mb-3">
	   <div class="col-12 mt-3">
		 <p class=" d-flex gap-3">Movers are motivated by the drive―the urgent need―to accomplish things. As a result, they
		  experience great satisfaction in seeing a task accomplished or a goal achieved. Once they reach that
		  goal, they are ready to move on to the next project, leaving any necessary tweaking and refining to
		  others. This does not mean that Movers do not care about quality. The results must definitely work
		  and fulfill the goal. But for most Movers, the outcome does not have to be perfect. Once the results
		  have demonstrated that they will obtain the intended outcomes, their interest is drawn to other
		  achievable goals.<img src="{{ asset('img/mover.png') }}" class="auto" alt="mapper" style="width: 130px;"></p>
		  
		  <p>Movers are drawn to tasks and activities that are doable in a short period of time. ”Short” is relative to the item in question. It
			could be a two-day project that will propel their team ahead in its larger goals. Or, it might be a three-month special assignment
			on a team that will be creating a new web-based process for interacting better with customers. The point is that Movers enjoy
			activities and tasks that have a defined, and relatively short, endpoint.</p>
		  <p>Movers generally don’t care who accomplishes the task. They are generally happy letting others do the work – as long as it is
			getting done efficiently. Therefore. Movers are often very good delegators, with strong social skills. They have a knack for
			instilling confidence in others, tied to a natural "mentoring" approach. Mentoring for the Mover does not constitute a long- term
			supportive relationship. It’s much more a, "let me show you how to do that" approach to mentoring. Once they feel the
			individual is up to speed, they are happy to let the person get it done.</p>
		  <p>At the same time, Movers naturally keep an eye on who is doing what on the team, and whether or not things are getting
			done with maximum efficiency. They may have to guard against a tendency either to take back work they have assigned to
			others when it is not getting done quickly enough, or to take over someone else’s work in order to speed up the process.</p>
		  <p>Movers are biased towards making quick decisions. They do so with an aura of decisiveness and confidence. They want to
			base their choice on solid facts and pertinent information. However, they are generally comfortable making the call once they
			have a general sense of what they think needs to happen. Lengthy discussions about possible options tend to annoy and
			frustrate them. They are inclined to believe that it is better just to decide and take action, than continue to deliberate.</p>
		  <p>This decision-making bias is grounded in the assumption that they can control for unforeseen problems or obstacles. This
			assumption gives them a sense of confidence, even when others are uncertain. This may appear somewhat reckless to others.
			However, Movers have usually taken a calculated risk, based on a natural talent for seeing what is actually achievable.</p>
		  <p>They also possess a natural talent for overcoming obstacles, either through their own sheer determination and effort, by
			sourcing new resources, or by recruiting the people they need to move ahead. The ability to overcome obstacles is a
			tremendous resource on a team – unless they see you as the obstacle! You then may feel the weight of their personality and
			persistence in trying to get you to change your mind, step aside, or just give up.</p>
		  <p class="mb-4">A common misperception of Movers is that they are difficult people to get along with–frequently bullying, bossing and
			disregarding others. That may be true of immature or highly stressed Movers. However, it is not true of the vast majority. In fact,
			Movers tend to be quite socially adept. They are at ease in meeting new people and playing the role of host or the “action
			orchestrator” at an event. Movers also show their social skill in using humor to defuse tense or stressful situations. They know
			how to play the political game, when necessary, to get the things they need. Their upbeat, can-do approach typically creates a
			positive environment for others. Their manner of relating often inspires others with confidence.</p>
