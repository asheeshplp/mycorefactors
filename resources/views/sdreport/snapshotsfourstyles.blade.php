	<h1>Snapshots of the Four Social Dynamics Styles</h1>
	  <p class="pb-2 pt-1">Read all four descriptions below and rank them:</p>
	  <p>1: Most Like You __________________  &nbsp;&nbsp;2: __________________&nbsp;&nbsp; 3: __________________&nbsp;&nbsp; 4: Least Like You __________________</p>

	<div class="row mt-5 mb-5">
	   <div class="col-12 d-flex">
		  <div class="left-info-snapshots w-50 border-snapshots-box border-end-0 border-bottom-0">
			  <p class="text-center text-black"><strong>Mover</strong></p>
			 <ul class="left-info-snapshots-list">
			   <li>An urgent need to acheive results, complete tasks and accomplish goals</li>
			   <li>Wants tangible, doable actions that can be taken "quickly"</li>
			   <li>Rapid decisions based on assessment of what needs to happen immediately or in the near future</li>
			   <li>Seeing things that can be done or achieved expeditiously</li>
			   <li>Quick, but realistic, decision-making</li>
			   <li>Taking immediate action</li>
			   <li>Willingness to act based on rapid risk assessment</li>
			   <li>Noticing what isn’t working</li>
			   <li>Overcoming problems, challenges, or obstacles to action</li>
			   <li>Staying focused under tight deadlines or in a crisis</li>
			   <li>Talking in a straightforward manner about issues, problems, concerns, and conflicts</li>
			 </ul>
		  </div>
		  <div class="right-info-snapshots w-50 border-snapshots-box border-bottom-0">
			<p class="text-center text-black"><strong>Involver</strong></p>
			<ul class="left-info-snapshots-list">
			  <li>An eager need to gain everyone's active involvement in an interaction</li>
			  <li>Wants enthusiastic agreement by everyone involved about goals, actions to be taken and tasks to do</li>
			  <li>Consensual decisions based on the active input of all individuals in the group</li>
			  <li>Bringing a positive, engaging energy to the interaction</li>
			  <li>Facilitating group discussion</li>
			  <li>Creating energy and activity to get things going and start everyone participating</li>
			  <li>Guiding the group towards consensus and gaining buy-in from everyone involved</li>
			  <li>Keeping motivation levels up as the team does its work through enthusiastic participation</li>
			  <li>Recognizing when someone’s energy has dropped, or they have become disengaged</li>
			  <li>Seeking to make things fun (but not frivolous)</li>
			</ul>
		</div>
	   </div>
	
	  <div class="col-12 d-flex">
		 <div class="left-info-snapshots w-50 border-snapshots-box border-end-0">
			 <p class="text-center text-black"><strong>Mapper</strong></p>
			<ul class="left-info-snapshots-list">
			  <li>An earnest need to formulate an effective course of action to achieve a beneficial goal</li>
			  <li>Wants clarity around the most useful goal and a clear and practical path to accomplish it</li>
			  <li>Carefully considered decisions about the most desirable action or goal</li>
			  <li>Gathering adequate information on which to determine goals and plans</li>
			  <li>Conceptualizing and articulating clear goals</li>
			  <li>Analyzing options to determine the best course of action to take</li>
			  <li>Anticipating problems, roadblocks, challenges and reactions</li>
			  <li>Organizing, sequencing and outlining timelines, actions, milestones, and procedures</li>
			  <li>Refocusing individuals and groups on the course of action</li>
			  <li>Guiding others to assist them in moving ahead</li>
			</ul>
		 </div>
		 <div class="right-info-snapshots w-50 border-snapshots-box">
		   <p class="text-center text-black"><strong>Integrator</strong></p>
		   <ul class="left-info-snapshots-list">
			  <li>A persistent need to integrate multiple sources of information in making choices</li>
			  <li>Wants useful information that can lead to the highest quality choice, output, or action</li>
			  <li>Informed decisions based on accessing high quality information</li>
			  <li>Working continually to gain information in order to make the best possible decision</li>
			  <li>Quietly encouraging everyone’s participation</li>
			  <li>Asking open-ended questions and listening closely for answers</li>
			  <li>Holding on to a great deal of information at once</li>
			  <li>Refining terms, concepts, frameworks, values and priorities</li>
			  <li>Thinking through carefully before acting, in order to achieve high quality the first time</li>
		   </ul>
	   </div>
