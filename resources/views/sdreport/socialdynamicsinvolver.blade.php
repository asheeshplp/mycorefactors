	<h1>The Involver Style</h1>
	  
	<div class="row mt-2 mb-3">
	   <div class="col-12 mt-3">
		  <div class="d-flex align-items-start gap-3">
		  <div class="rigt-para"><p>Involvers are motivated by the drive―the eager need―to gain everyone’s active participation in a
			discussion, working towards a shared decision. As a result, they experience great satisfaction when
			everyone participates enthusiastically, and the group reaches a decision to which everyone gives
			wholehearted endorsement. Involvers “want you to want to.” </p> <p>This does not mean that Involvers do not care about concrete results. They want to see things
			  get accomplished and care about the quality of the group’s output. However, they operate on the
			  assumption that the best way to achieve that quality and high levels of group productivity is by first
			  getting everyone on board with the decisions that precede action.
		  </p>
		 </div>
		 <img src="{{ asset('img/Involver.png') }}" class="auto" height="auto" alt="mapper" style="width: 130px;">
		</div>
		  
		  <p>Acting on this assumption, Involvers naturally facilitate a group’s process when they meet. This does not mean that they
			simply take over the leadership of the meeting. Rather, Involvers facilitate the group’s process by making statements like, “So,
			what should we do?” Or, “What’s everyone thinking about this?” Or perhaps, “Maybe we could spend some time brainstorming
			possibilities about that.” Involvers also notice those who haven’t spoken for a while and often will ask them directly for their
			thoughts on the matter at hand.
			</p>

		  <p>Involvers are biased towards consensual decisions that are made with a high degree of enthusiasm. Their lively, expressive
			non-verbal communication is engaging and serves to bring people into the group process. Their facilitative actions, mentioned
			above, directly elicit peoples’ ideas and involvement. Involvers also look for common ground between individuals in the
			discussion. These behaviors serve their desire for enthusiastic, consensual decision-making.</p>

		  <p>Although Involvers are seeking and acting to bring about a consensus, that does not mean they don’t have their own ideas
			about what the group should do. In fact, they may have a very strong opinion about the decision the group should take.
			However, they will work to persuade people to see their point of view, rather than seeking to impose their perspective.
			</p>

		  <p>While Involvers seek enthusiastic consensus, they do recognize that you can’t always get everyone to fully agree. Some
			people may not be very enthused about the group’s decision. Mature Involvers are willing to accept that sometimes, this is
			simply reality. Immature or stressed-out Involvers may not know when to stop trying to get someone on board with an idea,
			However, the vast majority know when to let things drop and will settle for a person’s willingness to at least not stand in the way
			of a decision.</p>

		  <p>When leading a group discussion, Involvers tend to view interaction as more important than structure. Therefore, while they
			can see value in an agenda, they often view it more as a guide, rather than a commitment of what will actually transpire at the
			meeting. They tend to follow the groups’ energy’. If a topic seems to be animating most of the members, they are liable to allow
			that discussion to continue, and either reduce the time spent on other items, or omit them altogether.
			</p>

		  <p>Involvers possess natural talents that support their drive to maximize involvement and collaboration. As noted above, they
			are natural facilitators. Many of them are also natural “brainstormers”, enjoying the process of building on others’ ideas. They
			are typically very articulate and persuasive about ideas and issues that are important to them. They tend to naturally encourage
			others and recognize them for their contribution to the team. They usually possess a good sense of humor. They employ it to
			create a positive environment, renew energy and keep things fun.
		  </p>

		  <p class="mb-4">Involvers are sometimes perceived to be frivolous. Their high energy, expressiveness and emphasis on fun can be
			interpreted as not being serious about business or achieving solid results. Involvers are actually serious and want the team to
			succeed. They just believe that people who are enjoying themselves will be more engaged and productive. They also believe
			that maximum buy-in to group decisions leads to maximum delivery of outstanding results.</p>                                  
		
