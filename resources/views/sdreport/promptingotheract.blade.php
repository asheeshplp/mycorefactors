	<h1>Prompting Others to Act: Prescribing and Describing</h1>

	<div class="row mt-2 mb-3">
	  <div class="col-12 mt-3">
		  <p>
			There are two broad forms of communication approaches available to prompt others to do something you want them to do.
			One form is to Prescribe the action you want someone to take by directly telling or asking the other person to do something for
			you. The other is to Describe the situation and explain what you want the other person to do or what needs to be done, without
			ever making a direct request to the other individual.                                   
		  </p> 
		  
			<div class="mt-5 mb-5">
			  <div class="text-black border-snapshots-box box-snapshot-bg mb-4">
				<p class="fw-semibold mb-0 py-3"><i>We all use some combination of both approaches in our interactions with others. However, we tend to use one more than
				  the other—particularly when acting spontaneously or under stress.</i></p>                                       
			  </div>
			  <p>The following is an example to begin to unpack the difference between the two means of prompting others to act.
			  </p>

			  <div class="border-snapshots-box border-2 rounded">
				<p class="mb-0">Let's say your car is in the shop and you would like to borrow your neighbor's car to drive your mother to her doctor's
				  appointment. What would you say to your neighbor?</p>
			  </div>
