	  <div id="prescribing-and-describing-prompting">
		  <div class="d-flex gap-4">
			  <div class="left-info">
				  <h4>Prescribing</h4>
				  <p>If you were to use a Prescribing form of communication to
					prompt your neighbor, you might say, "Could I please
					borrow your car to take my mom to the doctor's?" Or
					perhaps, simply, "Would you lend me your car for a couple
					of hours?" Or "I need to get my mom to her doctor's. Would
					you please loan me your car?" A blunter form might be,
					"Lend me your car," a wording we would suggest you use
					only in emergencies and crises, when good manners don't
					matter!</p>
				  
					<p>In all of these examples, you are prescribing a specific
					  action you want your neighbor to take. You are directly
					  asking them (or telling them!) to do something for you.</p>
			  </div>

			  <div class="right-info">
				  <h4>Describing</h4>
				  <p>If, on the other hand, you were to use a Describing
					approach, you might say, "I was wondering if I could borrow
					your car for a couple of hours." Or "I need a car to take my
					mom to her doctor's. What would you think about loaning
					me yours?" You might say, "Would you be able to loan me
					your car for a couple of hours to take my mom to her
					doctor's appointment?" A much more oblique form might
					simply be, "My car is in the shop and my mom has a
					doctor's appointment today."</p>
				  
					<p>In these examples, you are either describing what you
					  are thinking ("I was wondering…"), describing the situation
					  ("My car is in the shop…") or asking the other person to
					  describe their ability/availability to do something for you
					  ("Would you be able to…"). You never prescribe the other
					  person's actions; you never actually tell them what to do or
					  directly ask them to do something for you.
					  </p>
			  </div>
		  </div>
		  <div class="text-center my-5">
			<img class="w-75" src="{{ asset('img/img-14.png') }}" />
		  </div>
		  <p>The diagram on the previous page shows what the Social Dynamics styles share in common regarding prompting others to
			act. Movers and Mappers both prefer a Prescribing approach, and Involvers and Integrators both prefer a Describing Approach.
			When you can see the ways people prompt others to act it becomes easier to manage common misunderstandings in
			communication.</p>
