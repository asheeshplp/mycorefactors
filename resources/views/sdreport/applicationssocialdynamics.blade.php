<h1>Applications of Social Dynamics at Work</h1>
<div class="row mt-2 mb-3">
  <div class="col-12 mt-3">
	<p>
	  Social Dynamics can assist you in several
	  ways to bridge differences with others to
	  work more effectively and strengthen your
	  relationships. The applications on the
	  following pages are just a few of the many
	  practical ways that understanding and using
	  Social Dynamics can improve your ability to
	  form effective relationships, work
	  productively with others and enjoy work and
	  life even more. Use your knowledge of Social
	  Dynamics and the worksheet below to see how
	  you can shift perspectives and bridge your
	  differences with another individual.
	</p>

	<p>
	  Knowing what the styles share in common can
	  illuminate important sources of connection
	  with others and alternatively, what can
	  cause a disconnect. People tend to find it
	  easier to relate to and understand others
	  who share their preferences, and harder when
	  they don’t.
	</p>

	<div class="border-snapshots-box border-2 rounded mb-4 mt-4">
	  <p>
		For example: In a meeting, both Mover’s
		and Involvers will want a faster pace,
		more chances to interact and discuss
		things, minimal quiet times and a higher
		sustained degree of energy. Alternatively,
		Mappers and Integrators will both want
		opportunities for reflection, a chance to
		contemplate ideas on their own and moments
		when there is a calmer degree of energy.
		When it comes to issues around the pacing
		and energy when discussing an important
		issue in a meeting, you will naturally
		find alliances between Movers and
		Involvers on one side, and Mappers and
		Integrators on the other.
	  </p>
	</div>

	<p>
	  This does not mean that the styles cannot
	  relate to or work well with each other.
	  However, it does show why it is not uncommon
	  that there are particular challenges when
	  they attempt to do so. When they figure it
	  out, the results can be quite powerful.
	</p>
  </div>
</div>

