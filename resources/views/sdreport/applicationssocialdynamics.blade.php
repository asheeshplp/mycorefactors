<div class="row mt-3 mb-5" id="more-productive-conflict">
  <div class="col-md-12 mb-3">
	<h2 class="heading-inner-comman">Using Social Dynamics for More Productive Conflict</h2>
	<p class="mt-3">Although most of us have a negative perception of it, conflict is simply the presence of differences concerning something that
	  matters to the parties involved. It is inevitable in working relationships. It is actually valuable—if handled productively. Research
	  by Dr. Linda Hill of Harvard Business School demonstrates that it is indispensable for innovation. Unfortunately, conflict often
	  leads to misunderstandings, hurt feelings, lack of trust and ineffective working relationships.</p>
	
	<p>There can be many sources of differences that engender conflict at work—differing roles, past experiences, time pressures,
	  stress and more. A key source is differences in personality. This is where Social Dynamics Styles can provide valuable benefits.
	  This workshop will help you learn about common sources of conflict between Styles, typical approaches that each Style uses in
	  dealing with disagreements, specific methods for increasing mutual understanding and ways to use differences for improved
	  idea generation and problem solving.</p>
  </div>

  <div class="col-12 d-flex">
	<div class="left-info-snapshots w-50 border-snapshots-box border-end-0 border-bottom-0">
		<p class="text-center text-black"><strong>Mover</strong></p>
		<p class="fw-semibold text-black">Approach to Conflict</p>
	   <ul class="left-info-snapshots-list">
		 <li>First move is to deal directly with the person with
		  whom they are having the conflict</li>
		 <li>Prefer to discuss the issues frankly, looking for a
		  practical solution</li>
		 <li>Express directly their feelings and thoughts</li>
		 <li>If they are angry or worried about something
		  important, they may become aggressive and/or
		  dominating</li>
		 <li>May try to force the other to accede</li>
		 <li>May mistake the other person’s concession as buy-in
		  and be surprised when the other does not do as
		  expected</li>
	   </ul>
	</div>
	<div class="right-info-snapshots w-50 border-snapshots-box border-bottom-0">
	  <p class="text-center text-black"><strong>Involver</strong></p>
	  <p class="fw-semibold text-black">Approach to Conflict</p>
	  <ul class="left-info-snapshots-list">
		<li>First move is to seek areas of agreement to dissolve
		  tension</li>
		<li>Prefer to discuss the issue directly and resolve it
		  immediately</li>
		<li>Want to discuss feelings and opinions, not just facts
		</li>
		<li>Express their thoughts and feelings selectively,
		  monitoring the reaction of the other individual</li>
		<li>If they grow angry or frustrated, they may become
		  overly emotional and expressive</li>                                   
		<li>May try to force the other person to continue to
		  engage, even when that person needs or wants to
		  stop</li>
	  </ul>
  </div>
 </div>
 <div class="col-12 d-flex">
  <div class="left-info-snapshots w-50 border-snapshots-box border-end-0">
	  <p class="text-center text-black"><strong>Mapper</strong></p>
	  <p class="fw-semibold text-black">Approach to Conflict</p>
	 <ul class="left-info-snapshots-list">
	   <li>First move is to become quiet and avoid dealing
		directly with the issue in the moment</li>
	   <li>Prefer to wait to deal with the person after they have
		had time to think about their approach</li>
	   <li>When discussing the issue, they maintain a calm,
		non-emotional demeanor as long as possible</li>
	   <li>Prefer to focus on the facts, not the emotions of
		those involved</li>
	   <li>If the conflict intensifies, they may physically
		withdraw</li>
	  <li>May experience another person’s expression of
		emotions as inappropriate or unhelpful</li>
	 </ul>
  </div>
  <div class="right-info-snapshots w-50 border-snapshots-box">
	<p class="text-center text-black"><strong>Integrator</strong></p>
	<p class="fw-semibold text-black">Approach to Conflict</p>
	<ul class="left-info-snapshots-list">
	  <li>First move is to seek to accommodate the other
		individual and avoid the conflict</li>
	  <li>If they must deal with the issue, prefer to discuss it in
		a quiet, friendly manner</li>
	  <li>Tend to listen first and then try to reconcile what they
		have heard into a solution</li>
	  <li>If necessary, want to take a break for reflection and
		then revisit the issue</li>
	  <li>If they are rushed or pressured to act, they may
		attack others angrily</li>
	  <li>May frustrate the other by giving in, but then not
		following through</li>                                 
	</ul>
</div>
