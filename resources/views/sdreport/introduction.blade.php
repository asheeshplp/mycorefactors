	<h3>Your Social Dynamics Style Navigator Report</h3>
	<p>The content in this report reflects your responses to the Social Dynamics styles assessment that you completed online. The
	  assessment was developed by one of North America’s leading psychometricians. It has been tested and validated with many
	  individuals. Compared with other well-known personality type assessments, it has demonstrated an extremely high degree of
	  accuracy and reliability</p>
	<p>As with all self-report assessments, there is a slight possibility that the results of your assessment may not reflect your true
	  preferences and tendencies. Things like life experience and work or family role may have influenced your answers. Here are a
	  few other things that may influence your results:</p>
	
	  <ul class="list-disc">
		<li>your state of mind at the time you completed the assessment. For example, were you upset, anxious or excited about something at the time?</li>
		<li>the environment in which you filled it out—did you complete it while at work and were thinking about what your job requires rather than what is simply natural for you?</li>
		<li>your interpretation of specific words in the assessment might be different than what is intended.</li>
		<li>you may have been rushed to complete it because of other demands.</li>
	  </ul>
	
	  <p>While the likelihood is high that the results of the assessment do, in fact, reflect your natural preference, the content of this
		report, in addition to your practitioner, will help you confirm and validate your innate Social Dynamics style for yourself.</p>
	  
